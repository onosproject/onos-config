#!/bin/bash

# See ../docs/modelplugin.md for how to use this file to generate a Model Plugin

if [ $# -eq 0 ]
  then
    echo "No arguments supplied - expecting env file"
    exit 1
fi
source $1

if [ -z $TYPE ] ; then
    echo 'No TYPE given'
    exit 1
fi

if [ -z $VERSION ] ; then
    echo 'No VERSION given'
    exit 1
fi

if [ -z "$MODELDATA" ] ; then
    echo 'No MODELDATA given'
    exit 1
fi

#TYPEVERSION=Devicesim-1.0.0
#TYPEVERSIONPKG=devicesim_1_0_0
#TYPEMODULE=devicesim.so.1.0.0
TYPEVERSION=$TYPE-$VERSION
TYPELOWER=${TYPE,,}
VERSIONUS=${VERSION//[\.]/_}
TYPEVERSIONPKG=$TYPELOWER\_$VERSIONUS
TYPEMODULE=$TYPELOWER.so.$VERSION

mkdir -p $TYPEVERSION/$TYPEVERSIONPKG

createYangList() {
  IFS='\' read -r -a array <<< "$MODELDATA"
  for yang in "${array[@]}"
  do
    YANGNAME=$(echo $yang | awk -F ',' '{print $1}' | awk -F '"' '{print $2}')
    YANGORG=$(echo $yang | awk -F ',' '{print $2}' | awk -F '"' '{print $2}')
    YANGVER=$(echo $yang | awk -F ',' '{print $3}' | awk -F '"' '{print $2}')
    echo $YANGNAME@$YANGVER.yang" "
  done
}

if [ -z "$YANGLIST" ] ; then
  YANGLIST="$( createYangList )"
fi
echo "YANGLIST "$YANGLIST

go run $GOPATH/src/github.com/openconfig/ygot/generator/generator.go \
-path yang/$TYPEVERSION -output_file=$TYPEVERSION/$TYPEVERSIONPKG/generated.go -package_name=$TYPEVERSIONPKG \
-generate_fakeroot $YANGLIST

sed -i "1s/^/\/\/ Code generated by YGOT. DO NOT EDIT.\n/" $TYPEVERSION/$TYPEVERSIONPKG/generated.go


# Generate model

cat > $TYPEVERSION/modelmain.go << EOF
